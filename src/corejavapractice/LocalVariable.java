package corejavapractice;

public class LocalVariable {

	public static void a1() {
		// Local Variable
		int a = 20;
		int b = 30;
		System.out.println(a * b);
	}

	public static void b1() {
		// Local Variable
		int a = 60;
		int b = 70;
		System.out.println(a + b);
	}

	public static void main(String[] args) {

		a1();
		b1();
	}

}
