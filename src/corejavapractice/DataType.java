package corejavapractice;

public class DataType {

	public static void main(String[] args) {
		
		byte a=10;        // 1 byte 8 bit
		short b=10000; 	  // 2 byte 16 bit 
		int c=1000000000; // 4 byte 32 bit 
		long d=50000l;	  // 8 byte 64 bit
		double e=256.555D;// 8 byte 64 bit
		float f=25.55f;   // 4 byte 32 bit
		char g='a';       // 2 byte 16 bit
		boolean h2=20<10; //1 byte
		
		
		
		System.out.println("Value of byte =:>" +" "+ a);
		System.out.println("Value of short =:>" +" "+  b);
		System.out.println("Value of integer =:>" +" "+ c);
		System.out.println("Value of Long =:>" +" "+  d);
		System.out.println("Value of double =:>" +" "+ e);
		System.out.println("Value of Float =:>" +" "+ f);
		System.out.println("Value of Char =:>" +" "+ g);
		System.out.println("Value of boolean =:>" +" "+ h2);

	}

}
